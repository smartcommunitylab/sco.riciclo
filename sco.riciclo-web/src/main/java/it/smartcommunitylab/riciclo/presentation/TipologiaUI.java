package it.smartcommunitylab.riciclo.presentation;

public class TipologiaUI extends BaseObjectUI {

	private String id;
	private String nome;
	private String descrizione;
	private String icona;

	public TipologiaUI() {
	}

	public TipologiaUI(String id, String nome, String descrizione, String icona) {
		super();
		this.id = id;
		this.nome = nome;
		this.descrizione = descrizione;
		this.icona = icona;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	public String getIcona() {
		return icona;
	}

	public void setIcona(String icona) {
		this.icona = icona;
	}

	@Override
	public String toString() {
		return nome + ((descrizione != null) ? "=" + descrizione : "");
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TipologiaUI other = (TipologiaUI) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
