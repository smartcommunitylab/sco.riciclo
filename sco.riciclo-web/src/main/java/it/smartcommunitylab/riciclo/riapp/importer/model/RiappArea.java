package it.smartcommunitylab.riciclo.riapp.importer.model;

public class RiappArea {
	private String comune;
	private String comuneEsteso;
	private String zonaEsteso;
	private String cal;
	private String cen;
	private String diz;
	private String ist;
	private String varie;
	private String arac;
	private String codiceISTAT;
	
	public String getComune() {
		return comune;
	}
	public void setComune(String comune) {
		this.comune = comune;
	}
	public String getComuneEsteso() {
		return comuneEsteso;
	}
	public void setComuneEsteso(String comuneEsteso) {
		this.comuneEsteso = comuneEsteso;
	}
	public String getZonaEsteso() {
		return zonaEsteso;
	}
	public void setZonaEsteso(String zonaEsteso) {
		this.zonaEsteso = zonaEsteso;
	}
	public String getCal() {
		return cal;
	}
	public void setCal(String cal) {
		this.cal = cal;
	}
	public String getCen() {
		return cen;
	}
	public void setCen(String cen) {
		this.cen = cen;
	}
	public String getDiz() {
		return diz;
	}
	public void setDiz(String diz) {
		this.diz = diz;
	}
	public String getIst() {
		return ist;
	}
	public void setIst(String ist) {
		this.ist = ist;
	}
	public String getVarie() {
		return varie;
	}
	public void setVarie(String varie) {
		this.varie = varie;
	}
	public String getArac() {
		return arac;
	}
	public void setArac(String arac) {
		this.arac = arac;
	}
	public String getCodiceISTAT() {
		return codiceISTAT;
	}
	public void setCodiceISTAT(String codiceISTAT) {
		this.codiceISTAT = codiceISTAT;
	}
}
