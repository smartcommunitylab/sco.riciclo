package it.smartcommunitylab.riciclo.riapp.importer.model;

public class RiappRifiuto {
	private String rifiuto;
	private String dove;
	private String contenitore;
	private String pal;
	private String fr;
	
	public String getRifiuto() {
		return rifiuto;
	}
	public void setRifiuto(String rifiuto) {
		this.rifiuto = rifiuto;
	}
	public String getContenitore() {
		return contenitore;
	}
	public void setContenitore(String contenitore) {
		this.contenitore = contenitore;
	}
	public String getFr() {
		return fr;
	}
	public void setFr(String fr) {
		this.fr = fr;
	}
	public String getDove() {
		return dove;
	}
	public void setDove(String dove) {
		this.dove = dove;
	}
	public String getPal() {
		return pal;
	}
	public void setPal(String pal) {
		this.pal = pal;
	}
}
