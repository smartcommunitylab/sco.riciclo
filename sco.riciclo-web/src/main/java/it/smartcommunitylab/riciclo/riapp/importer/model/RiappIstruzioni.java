package it.smartcommunitylab.riciclo.riapp.importer.model;

public class RiappIstruzioni {
	private String frazione;
	private String comeConferire;
	private String prestaAttenzione;
	private String si;
	private String no;
	
	public String getFrazione() {
		return frazione;
	}
	public void setFrazione(String frazione) {
		this.frazione = frazione;
	}
	public String getComeConferire() {
		return comeConferire;
	}
	public void setComeConferire(String comeConferire) {
		this.comeConferire = comeConferire;
	}
	public String getPrestaAttenzione() {
		return prestaAttenzione;
	}
	public void setPrestaAttenzione(String prestaAttenzione) {
		this.prestaAttenzione = prestaAttenzione;
	}
	public String getSi() {
		return si;
	}
	public void setSi(String si) {
		this.si = si;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
}
