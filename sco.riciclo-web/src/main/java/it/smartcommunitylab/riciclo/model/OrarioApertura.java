package it.smartcommunitylab.riciclo.model;

public class OrarioApertura {

	private String dataDa;
	private String dataA;
	private String il;
	private String dalle;
	private String alle;
	private String eccezione;
	
	public String getDataDa() {
		return dataDa;
	}
	public void setDataDa(String dataDa) {
		this.dataDa = dataDa;
	}
	public String getDataA() {
		return dataA;
	}
	public void setDataA(String dataA) {
		this.dataA = dataA;
	}
	public String getIl() {
		return il;
	}
	public void setIl(String il) {
		this.il = il;
	}
	public String getDalle() {
		return dalle;
	}
	public void setDalle(String dalle) {
		this.dalle = dalle;
	}
	public String getAlle() {
		return alle;
	}
	public void setAlle(String alle) {
		this.alle = alle;
	}
	public String getEccezione() {
		return eccezione;
	}
	public void setEccezione(String eccezione) {
		this.eccezione = eccezione;
	}	
	
	
	
}
